def _wait_for_stage_load(ctx: Any, simulation_app: Any, *, max_updates: int = 6000) -> None:
    """Pump Kit updates until the (async) stage finishes loading.

    A multi-GB Infinigen USD streams in over many frames; traversing it before
    load completes finds zero floor meshes. ``get_stage_loading_status`` returns
    ``(message, files_loaded, files_to_load)`` — wait for nothing left to load.
    """
    for i in range(max_updates):
        status = ctx.get_stage_loading_status()
        if status[2] <= 0:
            logger.info("stage loaded after %d update(s)", i)
            return
        simulation_app.update()
    logger.warning("stage still loading after %d updates; proceeding", max_updates)


def _scene_dir_for(usd_path: Path) -> Path:
    """Resolve the per-scene directory (where occupancy.npy is cached)."""
    resolved = usd_path.resolve()
    for parent in resolved.parents:
        if parent.name.startswith("scene_") and parent.parent.name == "scenes":
            return parent
    return resolved.parent


def _format_matrix(rooms: list[dict[str, Any]], edges: list[dict[str, Any]]) -> str:
    mat = scene_connectivity.connectivity_matrix(len(rooms), edges)
    labels = [f"{i}:{r.get('room_type', '?')}" for i, r in enumerate(rooms)]
    width = max((len(l) for l in labels), default=3)
    header = " " * (width + 1) + " ".join(f"{i:>2}" for i in range(len(rooms)))
    lines = [header]
    for i, lab in enumerate(labels):
        cells = " ".join(" ." if not mat[i, j] else " #" for j in range(len(rooms)))
        lines.append(f"{lab:<{width}} {cells}")
    return "\n".join(lines)


def validate_connectivity(
    usd_path: Path,
    *,
    simulation_app: Any,
    cell_size: float = scene_connectivity.DEFAULT_RESOLUTION_M,
    pad_m: float = _DEFAULT_PAD_M,
    z_lo_m: float = _DEFAULT_Z_LO_M,
    z_hi_m: float = _DEFAULT_Z_HI_M,
    rasterize_fallback: bool = False,
    write: bool = True,
    occupancy_out: Path | None = None,
) -> dict[str, Any]:
    """Compute the connectivity graph for one scene and (optionally) author it.

    Returns a result dict with the rooms, edges, flags, and occupancy meta —
    suitable for logging/reporting whether or not ``write`` is set.
    """
    import omni.usd  # type: ignore
    from pxr import UsdPhysics  # type: ignore

    ctx = omni.usd.get_context()
    _trace(f"opening stage {usd_path}")
    if not ctx.open_stage(str(usd_path)):
        raise RuntimeError(f"omni.usd failed to open stage: {usd_path}")
    _wait_for_stage_load(ctx, simulation_app)
    stage = ctx.get_stage()
    _trace("stage ready; reading metadata + recovering rooms")

    metadata = scene_metadata_reader.read_custom_data(stage) or {}
    metadata.setdefault("rooms", [])
    metadata.setdefault("objects", [])
    metadata.setdefault("room_adjacency", [])

    recovered, floor_z = recover_rooms_from_floors(stage)
    backfilled_rooms = False
    rooms = metadata["rooms"]
    if not rooms:
        rooms = recovered
        metadata["rooms"] = rooms
        backfilled_rooms = True
        logger.info("Back-filled rooms[] from floor meshes (%d rooms)", len(rooms))
    if not rooms:
        raise RuntimeError("no rooms in metadata and none recoverable from floor meshes")

    doors = collect_door_prims(stage)
    bounds = _scene_bounds(rooms, pad_m)
    z_min, z_max = floor_z + z_lo_m, floor_z + z_hi_m
    _trace(
        f"rooms={len(rooms)} doors={len(doors)} bounds={tuple(round(b,2) for b in bounds)} "
        f"z=[{z_min:.2f},{z_max:.2f}] mode={'raster' if rasterize_fallback else 'omap'}"
    )

    # Ensure a physics scene exists for the occupancy raycasts. Track whether we
    # added it so we don't persist a scene-gen-only physics prim into the USD.
    added_physics = False
    physics_path = "/World/connectivityPhysicsScene"
    if not rasterize_fallback and not any(
        p.IsA(UsdPhysics.Scene) for p in stage.Traverse()
    ):
        from pxr import Sdf  # type: ignore

        UsdPhysics.Scene.Define(stage, Sdf.Path(physics_path))
        added_physics = True

    # Door-open guarantee: the mecanum strafer can't open doors, so the harness
    # authors them open at scene-gen time (the brief's "doors assumed open"). Drop
    # every door's collider BEFORE generating occupancy — doing it up front means
    # the single PhysX cook on timeline.play() never sees the closed panels, which
    # is robust where a post-gen RemoveAPI + re-cook is not. The leaf panel is also
    # hidden so the visual matches the now-open passage (a demo camera must not
    # film the robot driving through a shut-looking door). Both are persisted on
    # write so the runtime scene + occupancy match. (NOTE: the omap can still mark
    # a room sealed only by a meshSimplification-healed wall cutout as occupied —
    # a separate occupancy-fidelity limitation tracked as a follow-up, not a
    # door-matching gap.)
    forced_open = 0
    if not rasterize_fallback and doors:
        forced_open = drop_door_colliders(stage, [d["path"] for d in doors])
        hidden = hide_door_leaves(stage, [d["path"] for d in doors if d["is_leaf"]])
        for _ in range(3):
            simulation_app.update()
        _trace(
            f"force-opened {forced_open} door collider(s), hid {hidden} leaf mesh(es) "
            f"across {len(doors)} door prim(s)"
        )

    _trace("generating occupancy")
    occupancy, origin_xy = generate_occupancy_omap(
        simulation_app, bounds=bounds, z_min=z_min, z_max=z_max, cell_size=cell_size,
    ) if not rasterize_fallback else generate_occupancy_rasterize(
        stage, rooms, metadata, bounds=bounds, z_min=z_min, z_max=z_max, cell_size=cell_size,
    )
    _trace(f"occupancy shape={occupancy.shape} occupied={int(occupancy.sum())}")
    free = scene_connectivity.occupancy_to_free_space(occupancy, grid_res=cell_size)
    room_adjacency = metadata.get("room_adjacency") or None
    edges = scene_connectivity.compute_connectivity(
        rooms, free, grid_origin_xy=origin_xy, grid_res=cell_size,
        room_adjacency=room_adjacency,
    )
    cross_reach = sum(1 for e in edges if e.get("reachable") and e["from_idx"] != e["to_idx"])
    _trace(f"computed {len(edges)} edges ({cross_reach} cross-room reachable)")

    # Tag door_state on reachable edges: a door prim sitting on the crossing was
    # force-opened (this run or a prior one — re-processing an already-opened
    # scene drops 0 colliders but the door is still there), otherwise the passage
    # is an open gap. Keyed on a door at the crossing, not on this run's drop count.
    opened_doors = doors if not rasterize_fallback else []
    for e in edges:
        if not e.get("reachable"):
            continue
        e["door_state"] = "force-opened" if _door_at_crossing(e, opened_doors) else "absent"

    multi_story = scene_connectivity.is_multi_story(rooms)
    incompatible = scene_connectivity.is_multi_room_incompatible(edges)
    metadata["connectivity"] = edges
    metadata["multi_story"] = multi_story
    if incompatible:
        metadata["multi_room_incompatible"] = True
    else:
        metadata.pop("multi_room_incompatible", None)

    # Cache the occupancy sidecar (regenerable derived intermediate).
    scene_dir = _scene_dir_for(usd_path)
    st = usd_path.resolve().stat()
    occ_meta = scene_connectivity.occupancy_meta(
        origin_xy=origin_xy, resolution_m=cell_size, z_min_m=z_min, z_max_m=z_max,
        usd_path=str(usd_path.resolve()), usd_mtime_ns=st.st_mtime_ns, usd_size=st.st_size,
    )

    if write:
        if added_physics:  # don't persist the scene-gen-only physics prim
            stage.RemovePrim(physics_path)
        scene_metadata_reader.write_custom_data(stage, metadata)
        ctx.save_stage()
        scene_connectivity.save_occupancy(scene_dir, occupancy, occ_meta)
        _trace(f"authored connectivity[] into USD + cached occupancy in {scene_dir}")
    else:
        _trace("computed connectivity (no-write: USD + occupancy cache left untouched)")

    if occupancy_out is not None:  # debug/inspection dump, independent of --write
        occupancy_out.mkdir(parents=True, exist_ok=True)
        scene_connectivity.save_occupancy(occupancy_out, occupancy, occ_meta)
        (occupancy_out / "rooms.json").write_text(json.dumps(rooms, indent=2), encoding="utf-8")
        _trace(f"dumped occupancy + rooms to {occupancy_out}")

    return {
        "usd_path": str(usd_path),
        "rooms": rooms,
        "edges": edges,
        "multi_story": multi_story,
        "multi_room_incompatible": incompatible,
        "backfilled_rooms": backfilled_rooms,
        "occupancy_meta": occ_meta,
        "occupancy_shape": list(occupancy.shape),
    }


def _boot_kit() -> Any:
    """Boot a headless Isaac Sim Kit app with physics + the omap extension."""
    from isaaclab.app import AppLauncher  # type: ignore

    parser = argparse.ArgumentParser(add_help=False)
    AppLauncher.add_app_launcher_args(parser)
    kit_args = parser.parse_args([])
    kit_args.headless = True
    kit_args.enable_cameras = True
    app = AppLauncher(kit_args).app
    try:
        from isaacsim.core.utils.extensions import enable_extension  # type: ignore

        enable_extension("isaacsim.asset.gen.omap")
        app.update()
    except Exception as exc:  # noqa: BLE001
        logger.warning("could not enable isaacsim.asset.gen.omap: %s", exc)
    return app


def _cli_main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--usd", type=Path, required=True, help="Scene USD to process.")
    parser.add_argument("--cell-size", type=float, default=scene_connectivity.DEFAULT_RESOLUTION_M)
    parser.add_argument("--pad-m", type=float, default=_DEFAULT_PAD_M)
    parser.add_argument("--z-lo-m", type=float, default=_DEFAULT_Z_LO_M)
    parser.add_argument("--z-hi-m", type=float, default=_DEFAULT_Z_HI_M)
    parser.add_argument(
        "--rasterize-fallback", action="store_true",
        help="Build occupancy from room footprints + object AABBs instead of the "
             "occupancy-map extension (use when omap is unavailable).",
    )
    parser.add_argument(
        "--no-write", action="store_true",
        help="Compute + report only; do not author customData or cache occupancy.",
    )
    parser.add_argument("--json-out", type=Path, default=None, help="Write the result dict here.")
    parser.add_argument(
        "--occupancy-out", type=Path, default=None,
        help="Dump occupancy.npy + occupancy.json + rooms.json to this dir for "
             "inspection (independent of --no-write).",
    )
    parser.add_argument(
        "--log-file", type=Path, default=Path("/tmp/validate_scene_connectivity.log"),
        help="Mirror progress + errors here (Kit hijacks stdout, so a file log is "
             "the reliable trace).",
    )
    args = parser.parse_args(argv)

    logging.basicConfig(level=logging.INFO, format="%(levelname)s %(message)s")
    if not args.usd.exists():
        logger.error("USD not found: %s", args.usd)
        return 2

    global _TRACE_PATH
    _TRACE_PATH = args.log_file
    if _TRACE_PATH is not None:
        _TRACE_PATH.write_text("", encoding="utf-8")  # fresh trace per run

    _trace(f"booting Kit for {args.usd}")
    simulation_app = _boot_kit()
    rc = 0
    try:
        result = validate_connectivity(
            args.usd, simulation_app=simulation_app, cell_size=args.cell_size,
            pad_m=args.pad_m, z_lo_m=args.z_lo_m, z_hi_m=args.z_hi_m,
            rasterize_fallback=args.rasterize_fallback, write=not args.no_write,
            occupancy_out=args.occupancy_out,
        )
        # Persist the result inside the try — it lands before teardown either way.
        _report_result(result, args.json_out)
    except Exception:
        _trace("connectivity validation FAILED:\n" + traceback.format_exc())
        if args.json_out is not None:
            args.json_out.write_text(
                json.dumps({"error": traceback.format_exc(), "usd_path": str(args.usd)}, indent=2),
                encoding="utf-8",
            )
        rc = 1

    # Kit's simulation_app.close() reliably hangs/segfaults on teardown here and
    # leaves a zombie holding the GPU, which collides with the next launch. The
    # USD save + occupancy cache + result JSON already landed above, so hard-exit
    # and let the OS reclaim Kit (the same os._exit pattern the lab Kit tests use).
    _trace(f"exiting rc={rc}")
    sys.stdout.flush()
    sys.stderr.flush()
    os._exit(rc)


def _report_result(result: dict[str, Any], json_out: Path | None) -> None:
    rooms, edges = result["rooms"], result["edges"]
    reachable = sum(1 for e in edges if e.get("reachable"))
    cross_reach = sum(1 for e in edges if e.get("reachable") and e["from_idx"] != e["to_idx"])
    matrix_str = _format_matrix(rooms, edges)
    _trace(
        f"{Path(result['usd_path']).name}: {len(rooms)} rooms, {len(edges)} edges "
        f"({reachable} reachable, {cross_reach} cross-room), "
        f"multi_story={result['multi_story']} "
        f"multi_room_incompatible={result['multi_room_incompatible']}"
    )
    _trace("Connectivity matrix (# = reachable):\n" + matrix_str)
    _trace("Edges:\n" + "\n".join("  " + json.dumps(e) for e in edges))
    print("\nConnectivity matrix (# = reachable):\n" + matrix_str, flush=True)
    if json_out is not None:
        json_out.write_text(json.dumps(result, indent=2), encoding="utf-8")
        _trace(f"wrote result to {json_out}")


if __name__ == "__main__":  # pragma: no cover
    sys.exit(_cli_main())
