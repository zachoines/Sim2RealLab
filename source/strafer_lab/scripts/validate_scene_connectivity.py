# Door-open verification + door-state tagging
# ---------------------------------------------------------------------------


def drop_door_colliders(stage: Any, door_paths: list[str]) -> int:
    """Remove collision APIs from door prims (and their mesh children)."""
    from pxr import UsdPhysics  # type: ignore

    count = 0
    for path in door_paths:
        root = stage.GetPrimAtPath(path)
        if not root or not root.IsValid():
            continue
        for prim in [root, *root.GetChildren()]:
            had = False
            if prim.HasAPI(UsdPhysics.MeshCollisionAPI):
                prim.RemoveAPI(UsdPhysics.MeshCollisionAPI)
                had = True
            if prim.HasAPI(UsdPhysics.CollisionAPI):
                prim.RemoveAPI(UsdPhysics.CollisionAPI)
                had = True
            count += int(had)
    return count


def hide_door_leaves(stage: Any, leaf_paths: list[str]) -> int:
    """Make the door-leaf (swinging panel) meshes invisible.

    A dropped collider makes the doorway passable, but the panel mesh stays
    shut visually — a camera capturing a demo would film the robot driving
    *through* a closed-looking door, teaching "closed doors are passable". So
    the leaf panel is hidden (the frame casing stays) and the doorway reads as
    an open passage. Persisted on save.
    """
    from pxr import UsdGeom  # type: ignore

    count = 0
    for path in leaf_paths:
        prim = stage.GetPrimAtPath(path)
        if not prim or not prim.IsValid():
            continue
        UsdGeom.Imageable(prim).MakeInvisible()
        count += 1
    return count


def _door_at_crossing(edge: dict[str, Any], doors: list[dict[str, Any]]) -> bool:
    """True if a door prim sits on this edge's doorway crossing point."""
    via = edge.get("via_doorway_xy")
    if not via:
        return False
    vx, vy = via
    return any(
        math.hypot(d["center_xy"][0] - vx, d["center_xy"][1] - vy) <= _DOOR_NEAR_DOORWAY_M
        for d in doors
    )


