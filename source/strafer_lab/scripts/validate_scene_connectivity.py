# Occupancy generation
# ---------------------------------------------------------------------------


def _occupancy_from_points(
    points: Any, *, bounds: tuple[float, float, float, float], cell_size: float
) -> tuple[np.ndarray, tuple[float, float]]:
    """Rasterize occupied world positions into a planner-convention grid.

    ``grid[row, col]`` with ``row`` indexing world +X, ``col`` +Y, origin at
    ``(min_x, min_y)``. 1 = occupied.
    """
    min_x, min_y, max_x, max_y = bounds
    nx = max(1, int(math.ceil((max_x - min_x) / cell_size)))
    ny = max(1, int(math.ceil((max_y - min_y) / cell_size)))
    grid = np.zeros((nx, ny), dtype=np.uint8)
    for p in points:
        r = int((float(p[0]) - min_x) / cell_size)
        c = int((float(p[1]) - min_y) / cell_size)
        if 0 <= r < nx and 0 <= c < ny:
            grid[r, c] = 1
    return grid, (min_x, min_y)


def generate_occupancy_omap(
    simulation_app: Any,
    *,
    bounds: tuple[float, float, float, float],
    z_min: float,
    z_max: float,
    cell_size: float,
) -> tuple[np.ndarray, tuple[float, float]]:
    """Generate occupancy via the Isaac Sim occupancy-map extension.

    Reads the stage's physics colliders over the z-slab ``[z_min, z_max]`` and
    rasterizes the occupied cells into a planner-convention grid.
    """
    import omni.timeline  # type: ignore
    from isaacsim.asset.gen.omap.bindings import _omap  # type: ignore
    from isaacsim.asset.gen.omap.utils import update_location  # type: ignore

    min_x, min_y, max_x, max_y = bounds
    om = _omap.acquire_omap_interface()
    timeline = omni.timeline.get_timeline_interface()

    # Slab anchored at the min corner; bounds are relative to that origin.
    update_location(
        om,
        (min_x, min_y, z_min),
        (0.0, 0.0, 0.0),
        (max_x - min_x, max_y - min_y, z_max - z_min),
    )
    om.set_cell_size(cell_size)

    timeline.play()
    # Kit settle-pumps (empirical / conservative, tied to the Kit-timeline
    # gotchas): 8 frames let PhysX cook the colliders + the door-collider drop
    # propagate before the map is built; 2 frames flush the omap result before we
    # read it. Too few risks an occupancy from a half-cooked scene.
    for _ in range(8):
        simulation_app.update()
    om.generate()
    for _ in range(2):
        simulation_app.update()
    points = om.get_occupied_positions()
    timeline.stop()
    logger.info("omap generated %d occupied cells", len(points))
    return _occupancy_from_points(points, bounds=bounds, cell_size=cell_size)


def generate_occupancy_rasterize(
    stage: Any,
    rooms: list[dict[str, Any]],
    metadata: dict[str, Any],
    *,
    bounds: tuple[float, float, float, float],
    z_min: float,
    z_max: float,
    cell_size: float,
) -> tuple[np.ndarray, tuple[float, float]]:
    """Fallback: rasterize room footprints + object AABBs into occupancy.

    Documented fallback for when the occupancy-map extension is unavailable.
    Marks everything outside the room footprints as occupied (so the exterior
    is non-navigable), then carves object AABBs (intersecting the z-slab) back
    in as occupied obstacles. Misses collision-only doorway geometry the omap
    path captures — hence the fallback, not the default.
    """
    min_x, min_y, max_x, max_y = bounds
    nx = max(1, int(math.ceil((max_x - min_x) / cell_size)))
    ny = max(1, int(math.ceil((max_y - min_y) / cell_size)))
    # Start fully occupied; free out the interior of each room footprint.
    grid = np.ones((nx, ny), dtype=np.uint8)
    xs = (np.arange(nx) + 0.5) * cell_size + min_x
    ys = (np.arange(ny) + 0.5) * cell_size + min_y
    gx, gy = np.meshgrid(xs, ys, indexing="ij")
    for room in rooms:
        fp = room["footprint_xy"]
        rx = [p[0] for p in fp]
        ry = [p[1] for p in fp]
        inside = (gx >= min(rx)) & (gx <= max(rx)) & (gy >= min(ry)) & (gy <= max(ry))
        grid[inside] = 0
    # Re-occupy object AABBs that intersect the slab.
    for obj in metadata.get("objects", []) or []:
        bmin = obj.get("bbox_3d_min") or obj.get("bbox_3d", {}).get("min")
        bmax = obj.get("bbox_3d_max") or obj.get("bbox_3d", {}).get("max")
        if not bmin or not bmax:
            continue
        if bmax[2] < z_min or bmin[2] > z_max:
            continue
        inside = (gx >= bmin[0]) & (gx <= bmax[0]) & (gy >= bmin[1]) & (gy <= bmax[1])
        grid[inside] = 1
    return grid, (min_x, min_y)


