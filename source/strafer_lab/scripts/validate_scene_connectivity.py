"""Compute + author a scene's verified room connectivity graph (Kit-bound).

Sibling to ``extract_scene_metadata.py`` — kept separate so the
Infinigen-state-walker stays pure. This step runs *after* metadata authoring
in the scene-gen pipeline and produces two artifacts:

1. A cached occupancy grid (``<scene>/occupancy.npy`` + ``occupancy.json``).
   Generated by Isaac Sim's occupancy-map extension (Isaac Sim 6.x
   ``isaacsim.asset.gen.omap``; the 5.x ``omni.isaac.occupancy_map`` is its
   predecessor), which reads the stage's *physics colliders* — so it sees the
   doorway free-space the floor/object-AABB rasterizer would miss. This grid
   is a regenerable derived intermediate (not authored metadata): ``occupancy
   .json`` records the grid→world mapping plus the source-USD identity so a
   stale grid is detectable. It is the **shared occupancy seam** the other
   Infinigen path-planning consumers (mission-generator oracle,
   grounding-negative trajectory checks) load instead of re-rasterizing — see
   :mod:`strafer_lab.tools.scene_connectivity` and
   ``docs/tasks/context/path-planning-architecture.md``.

2. A ``connectivity[]`` block + ``multi_story`` flag merged into the scene
   USD's root-prim ``customData`` (the authored metadata; no JSON sidecar).
   Each edge is an undirected room pair (``from_idx`` < ``to_idx``, indices
   into ``rooms[]`` — not ``room_type`` strings, since rooms can share a
   type):

       reachable:   {from_idx, to_idx, reachable: true, via_doorway_xy,
                     path_length_m, door_state}
       unreachable: {from_idx, to_idx, reachable: false, reason}

   ``reason`` is ``"stairs"`` (cross-story; the mecanum strafer can't climb)
   or ``"blocked"`` (no collision-free path even with doors open). The strafer
   has no manipulator, so every door is opened at scene-gen time (the brief's
   "doors assumed open"): the collider is dropped *before* occupancy generation
   and the leaf panel mesh is hidden so the visual matches the open passage.
   Both are persisted. ``door_state`` on a reachable edge is then
   ``"force-opened"`` (a door prim sits on the crossing) or ``"absent"`` (an
   open gap, no door). Cross-story scenes also set ``multi_story: true`` at the
   top level. A scene is tagged ``multi_room_incompatible: true`` only when it
   has cross-room candidate pairs but *none* is reachable.

   A room sealed only by a doorway whose wall-cutout the postprocess
   ``meshSimplification`` collider heals shut can still read as occupied (the
   omap's flood treats the enclosed interior as occupied, not free); that is a
   known occupancy-fidelity limitation tracked as a follow-up, not a
   door-matching gap.

Reachability is decided by the project's one shared grid planner
(``plan_path``) over the inflated occupancy — this brief writes a grid
adapter, not a second A*.

Room geometry: the per-room footprints come from the authored ``rooms[]``
when present; otherwise they are recovered from the per-room Infinigen floor
meshes (``<room_type>_<story>_<index>_floor``) and back-filled into the
metadata, so a scene whose metadata was authored from USD prim names alone
(``rooms == []``) still gets a connectivity graph.

If the occupancy-map extension is unavailable, pass ``--rasterize-fallback``
to build the occupancy from the room footprints + object AABBs instead (the
brief's documented fallback); the output is the same free-space grid.

Usage::

    $ISAACLAB -p source/strafer_lab/scripts/validate_scene_connectivity.py \\
        --usd Assets/generated/scenes/<scene>.usdc
"""

