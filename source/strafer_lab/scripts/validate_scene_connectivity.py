# Kit replaces the root logging config on boot and redirects sys.stdout, so the
# standard logger is unreliable for milestones. _trace writes straight to a file
# (and the *original* stderr) which Kit cannot clobber.
_TRACE_PATH: Path | None = None


def _trace(msg: str) -> None:
    logger.info(msg)
    line = f"{datetime.datetime.now().isoformat()} {msg}\n"
    try:
        sys.__stderr__.write(line)
        sys.__stderr__.flush()
    except Exception:  # noqa: BLE001
        pass
    if _TRACE_PATH is not None:
        with open(_TRACE_PATH, "a", encoding="utf-8") as fh:
            fh.write(line)

# Infinigen door factories (PanelDoor / LiteDoor / LouverDoor / GlassPanelDoor,
# both the frame ``__spawn_asset_N_`` and leaf ``__spawn_asset_N__001`` prims).
# Verified against seed1/seed2: this catches every door-named prim in both scenes
# (incl. the GlassPanel variant), so there is no door the door-open pass misses.
_DOOR_PRIM_RE = re.compile(r"Door[A-Za-z]*Factory_\d+__spawn_asset_\d+_")
# The leaf is the swinging panel (extra ``__NNN`` suffix); the frame is the casing.
_DOOR_LEAF_RE = re.compile(r"__spawn_asset_\d+__\d+$")

# Occupancy slab, measured above the floor.
#   z_lo: just above the floor — clears the floor plane (its colliders are stripped
#         in postprocess anyway) plus any door-sill / transition-strip lip.
#   z_hi: the robot's vertical envelope (floor → top of the camera mast). An
#         obstacle in this band can collide; anything above it (a tabletop) the
#         robot drives under, so the slab stops there. Derived from the shared
#         chassis constants so it tracks the real robot, not a magic number.
try:
    from strafer_shared.constants import (  # noqa: WPS433
        CAMERA_HEIGHT,
        CAMERA_OFFSET_Z,
        CHASSIS_GROUND_CLEARANCE,
        CHASSIS_HEIGHT,
    )

    _ROBOT_TOP_M = (
        CHASSIS_GROUND_CLEARANCE + CHASSIS_HEIGHT + CAMERA_OFFSET_Z + CAMERA_HEIGHT
    )
except Exception:  # pragma: no cover - strafer_shared always present in lab env
    _ROBOT_TOP_M = 0.34
_DEFAULT_Z_LO_M = 0.05
_DEFAULT_Z_HI_M = round(_ROBOT_TOP_M, 3)  # ~0.34 m above the floor

# Occupancy bounds are padded past the floor-mesh AABBs so the room walls (which
# sit at / just outside the floor edge) land inside the grid rather than being
# clipped at the boundary. 1.5 m comfortably exceeds wall thickness + a doorway's
# outward reach.
_DEFAULT_PAD_M = 1.5

# A door prim counts as "on" a doorway crossing if its center is within this of
# the path's estimated crossing point: ~half a standard door width plus the
# crossing-estimate slop, tight enough not to grab a neighbouring door.
_DOOR_NEAR_DOORWAY_M = 0.7


# ---------------------------------------------------------------------------
# USD geometry recovery (pxr / Kit)
# ---------------------------------------------------------------------------


def _xy_bbox(prim: Any, bbox_cache: Any) -> tuple[float, float, float, float, float] | None:
    """Return ``(min_x, min_y, max_x, max_y, mid_z)`` world AABB or ``None``."""
    try:
        rng = bbox_cache.ComputeWorldBound(prim).ComputeAlignedRange()
        if rng.IsEmpty():
            return None
        mn, mx = rng.GetMin(), rng.GetMax()
        return float(mn[0]), float(mn[1]), float(mx[0]), float(mx[1]), float(0.5 * (mn[2] + mx[2]))
    except Exception:  # noqa: BLE001 - USD bbox can throw on malformed prims
        return None


def recover_rooms_from_floors(stage: Any) -> tuple[list[dict[str, Any]], float]:
    """Recover ``rooms[]`` from per-room Infinigen floor meshes.

    Returns ``(rooms, floor_z)`` where each room carries ``room_type``,
    ``story``, ``footprint_xy`` (the floor mesh's XY AABB as a rectangle) and
    ``area_m2``. ``floor_z`` is the median floor height (the occupancy z-slab
    reference).
    """
    from pxr import Usd, UsdGeom  # type: ignore

    bbox_cache = UsdGeom.BBoxCache(
        Usd.TimeCode.Default(), [UsdGeom.Tokens.default_], useExtentsHint=True
    )
    rooms: list[dict[str, Any]] = []
    floor_zs: list[float] = []
    for prim in stage.Traverse():
        if not prim.IsValid() or prim.GetTypeName() != "Mesh":
            continue
        parsed = scene_connectivity.parse_room_floor_name(prim.GetName())
        if parsed is None:
            continue
        box = _xy_bbox(prim, bbox_cache)
        if box is None:
            continue
        min_x, min_y, max_x, max_y, mid_z = box
        room_type, story, _index = parsed
        footprint = scene_connectivity.aabb_to_footprint((min_x, min_y), (max_x, max_y))
        rooms.append(
            {
                "room_type": room_type,
                "story": story,
                "footprint_xy": footprint,
                "area_m2": round((max_x - min_x) * (max_y - min_y), 4),
            }
        )
        floor_zs.append(mid_z)
    floor_z = float(np.median(floor_zs)) if floor_zs else 0.0
    logger.info("Recovered %d rooms from floor meshes (floor_z=%.3f)", len(rooms), floor_z)
    return rooms, floor_z


def collect_door_prims(stage: Any) -> list[dict[str, Any]]:
    """Return door prims with their XY AABB center + leaf flag.

    ``center_xy`` is used for door_state tagging; ``is_leaf`` (the swinging
    panel, ``__NNN`` suffix) is hidden so the visual matches the now-passable
    doorway.
    """
    from pxr import Usd, UsdGeom  # type: ignore

    bbox_cache = UsdGeom.BBoxCache(
        Usd.TimeCode.Default(), [UsdGeom.Tokens.default_], useExtentsHint=True
    )
    doors: list[dict[str, Any]] = []
    seen: set[str] = set()
    for prim in stage.Traverse():
        if not prim.IsValid():
            continue
        name = prim.GetName()
        if not _DOOR_PRIM_RE.search(name):
            continue
        if name in seen:  # collapse the Xform/Mesh twin
            continue
        box = _xy_bbox(prim, bbox_cache)
        if box is None:
            continue
        seen.add(name)
        min_x, min_y, max_x, max_y, _mid_z = box
        doors.append(
            {
                "path": str(prim.GetPath()),
                "center_xy": (0.5 * (min_x + max_x), 0.5 * (min_y + max_y)),
                "is_leaf": bool(_DOOR_LEAF_RE.search(name)),
            }
        )
    logger.info("Found %d door prims (%d leaves)", len(doors), sum(d["is_leaf"] for d in doors))
    return doors


def _scene_bounds(rooms: list[dict[str, Any]], pad_m: float) -> tuple[float, float, float, float]:
    pts = np.concatenate([np.asarray(r["footprint_xy"], dtype=float) for r in rooms])
    return (
        float(pts[:, 0].min()) - pad_m,
        float(pts[:, 1].min()) - pad_m,
        float(pts[:, 0].max()) + pad_m,
        float(pts[:, 1].max()) + pad_m,
    )


